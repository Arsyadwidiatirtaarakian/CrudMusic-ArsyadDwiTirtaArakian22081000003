    private void CariData(){
    try{
        st = cn.createStatement();
        rs = st.executeQuery("SELECT * FROM crudmusic WHERE " 
                             + cmbCari.getSelectedItem().toString() + 
                              " LIKE '%" + txtCari.getText() + "%'");
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Lagu");
        model.addColumn("Artis");
        model.addColumn("Album");
        
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        model.setRowCount(0);
        
        while (rs.next()){
           Object [] data = {
               rs.getString("no"),
               rs.getString("lagu"),
               rs.getString("artis"),
               rs.getString("album")
           };
           model.addRow(data);
           tblData.setModel(model);
        }   
        } catch (Exception e) {
        
        }
    
    }
